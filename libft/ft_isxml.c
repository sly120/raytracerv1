#include "libft.h"

int			ft_isxml(int c)
{
	return (ft_isalnum(c) || c == '\n' || c == '\0' || c == '-' || c == ' ' ||
			c == '{' || c == '}' || c == ',' || c == '<' || c == '>');
}
